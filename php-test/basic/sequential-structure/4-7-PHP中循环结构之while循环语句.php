<?php
$sum = 12;//小宠物当前的饥饿程度
echo "我饿啦:-(";
echo "<br />";
while($sum<100){//小宠物的饥饿程度到100，表示小宠物吃饱啦,不用继续喂了，没吃饱继续喂食
    $num = rand(1,20);//随机数，模拟喂食小宠物的小面包
	$sum = $sum + $num; //小宠物吃小面包
	echo "我还没吃饱呢！";
	echo "<br />";
}
echo "终于吃饱啦^_^";


/*
